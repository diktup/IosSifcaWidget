import SwiftUI

extension Font {
    static func customBodyFont() -> Font {
        return Font.custom("Rubik-medium", size: 14)
    }

    static func customHeadlineFont() -> Font {
        return Font.custom("Rubik-Bold", size: 20)
    }
}
